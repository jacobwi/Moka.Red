namespace Moka.Red.Core.Enums;

/// <summary>Align-items alignment for flex layouts.</summary>
public enum MokaAlign
{
	/// <summary>Align items to the start of the cross axis.</summary>
	Start,

	/// <summary>Center items along the cross axis.</summary>
	Center,

	/// <summary>Align items to the end of the cross axis.</summary>
	End,

	/// <summary>Stretch items to fill the cross axis.</summary>
	Stretch,

	/// <summary>Align items along their text baseline.</summary>
	Baseline
}
