namespace Moka.Red.Core.Enums;

/// <summary>Justify-content alignment for flex layouts.</summary>
public enum MokaJustify
{
	/// <summary>Pack items toward the start.</summary>
	Start,

	/// <summary>Center items along the main axis.</summary>
	Center,

	/// <summary>Pack items toward the end.</summary>
	End,

	/// <summary>Distribute items with equal space between them.</summary>
	SpaceBetween,

	/// <summary>Distribute items with equal space around them.</summary>
	SpaceAround,

	/// <summary>Distribute items with equal space between and around them.</summary>
	SpaceEvenly
}
